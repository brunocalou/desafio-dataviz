export class CompetitionRecord {
  constructor ({
    year,
    country,
    countryAbbreviation,
    event,
    gender,
    mark,
    athleteName
  }) {
    this.year = year
    this.country = country
    this.countryAbbreviation = countryAbbreviation
    this.event = event
    this.gender = gender
    this.mark = mark
    this.athleteName = athleteName
  }
}
